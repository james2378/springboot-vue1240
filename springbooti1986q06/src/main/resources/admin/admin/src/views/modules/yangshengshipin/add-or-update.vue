							this.ruleForm.shipinneirong = obj[o];
							this.ro.shipinneirong = true;
							continue;
						}
						if(o=='faburiqi'){
							this.ruleForm.faburiqi = obj[o];
							this.ro.faburiqi = true;
							continue;
						}
